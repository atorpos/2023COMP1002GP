// When mouse over, change the image
function mouseOn(){
    var obj = document.getElementById("defalut-mario");
    obj.src = "images/1200px-NSMB2_Invincible_Mario_Artwork.png";
}

//When mouse out, back to default image
function mouseOff(){
    var obj = document.getElementById("defalut-mario");
    obj.src = "images/mariogroup.png";
}